package com.parkinglot.domain.models.Account;

public class Person {
    private String name;
    private String address;
    private String email;
    private String phone;

    public Person(String name, String address, String email, String phone) {
        this.name = name;
        this.address = address;
        this.email = email;
        this.phone = phone;
    }
}
